    private void sjekkpassord() {
        if (Sonja.sjekkbruker(jTextField1.getText(),
                jPasswordField1.getPassword())) {
            this.dispose();
            godkjent = true;
        } else {
            Sonjavindu.melding(
                    "OBS. ",
                    "Brukernavn/passord var feil!\n");
            forsok++;
        }
    }

    private void sjekkpassordold() {
        if (Sonja.sjekkbruker(jTextField1.getText(), jPasswordField1.getPassword())) {
            this.dispose();
        } else {
            if (forsok > 0) {
                forsok--;
                Sonjavindu.melding(
                        "Obs",
                        "Kombinasjonen brukernavn/passord var feil!\n"
                        + "Du har " + forsok + " fors√∏k til");
                if (forsok == 0) {
                    System.exit(0);
                    //this.dispose();
                }
            } else {
                System.exit(0);
            }
        }
    }
    
    public boolean kontroll(){
        boolean retval = false;
        while (!godkjent && forsok < 3){
           retval = godkjent;
        }
        retval = godkjent;
        return retval;
    }
